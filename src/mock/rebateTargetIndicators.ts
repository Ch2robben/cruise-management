export function calcIndicatorCompletionRate(indicator: RebateTargetIndicator) {
  if (indicator.targetValue <= 0) return 0
  return Math.round((indicator.actualValue / indicator.targetValue) * 1000) / 10
}

export function getIndicatorById(id: string) {
  return rebateTargetIndicatorSeeds.find((item) => item.id === id)
}

export function listIndicatorsForDealer(dealerId: string) {
  return rebateTargetIndicatorSeeds.filter((item) => item.dealerId === dealerId && item.status === 'enabled')
}
