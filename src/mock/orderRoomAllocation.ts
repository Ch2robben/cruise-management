import type { OrderRoomLine } from '@/components/order/orderTypes'

function guest(
  lineId: string,
  slot: number,
  name: string,
  price: number,
): OrderRoomLine['guests'][number] {
  return {
    id: `${lineId}-g${slot}`,
    slotIndex: slot,
    name,
    ageGroup: '成人',
    occupancyType: '正常',
    gender: slot % 2 === 1 ? '男' : '女',
    idType: '身份证',
    idNumber: '-',
    phone: slot === 1 ? '13800000000' : '-',
    settlementPrice: price,
    priceCoefficient: 1,
    isCompanion: false,
    transferRequired: '否',
  }
}

